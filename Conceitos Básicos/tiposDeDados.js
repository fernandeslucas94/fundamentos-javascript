/// O que são tipos de dados ? Como o próprio nome já diz, são maneiras que podemos identificar e categorizar alguma informação. Temos vários tipos de dados, e veremos aqui os que mais utilizamos no Javascript.
//
/// Podemos classificar os tipos de dados em: 
//
// String = texto
// Number = números que sejam inteiros, ou seja, não possuem casa decimal (1, 2, 50, 100, etc...)
// Float = números que podem possuir casas decimas (1.5, 2.7, 50.3, 103.9, etc...)
// Boolean = com o tipo boolean, ou bool em algumas linguagens, podemos ter apenas dois resultados: true/verdadeiro ou false/falso. Utilizamos o boolean para determinarmos se alguma informação é verdadeira ou falsa, como por exemplo, em um caso de validação de formulário ou login.